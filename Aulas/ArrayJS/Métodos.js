const heroisDC = ["Batman", "Superman", "Besouro Azul", "Flash"];
heroisDC.pop(); //Remove o ultimo elemento do array
console.log(heroisDC);

heroisDC.push("Lanterna Verde"); //Adciona no ultimo elemento
console.log(heroisDC);

heroisDC.shift() //Remove o primeiro elemento
console.log(heroisDC)

heroisDC.unshift("SuperChoque") //Adciona no primeiro elemento
console.log(heroisDC)

//Splice pode tanto adcionar como remover elementos

